console.log("Hello World"); // This will print "Hello World" in the console
console.error("Hey, this is an error"); // This will give an error in the console with the message "Hey, this is an error"

// Assert will give an error if the condition is false
console.assert(5>553); // This will give an error in the console with the message "Assertion failed"
console.assert(5<553); // This will not give any error in the console

console.clear(); // This will clear the console

obj = {a:1, b:2, c:3, d:4, e:5};

console.table(obj); // This will print the object in the form of a table

console.warn("Hey, please don't drink soda..."); // This will give a warning in the console with the message "Hey, please don't drink soda..."

console.info("This is a info message"); // This will give an info message in the console with the message "This is a info message"

console.time("Time taken"); // This will start the timer

console.timeEnd("Time taken"); // This will end the timer and print the time taken in the console